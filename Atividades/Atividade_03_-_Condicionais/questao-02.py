"""

Questão 2 - Escreve um programa que dada uma String A, substitua todas as ocorrências de "banana" por "Maçã"

"""

# Solicitando a entrada do usuário
string = input("Digite uma frase: ")

# Substituindo todas as ocorrências de "banana" por "Maçã"
string = string.replace("a", "Maçã")

# Exibindo a string modificada
print(string)


